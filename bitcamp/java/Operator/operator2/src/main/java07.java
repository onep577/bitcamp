package main;

public class java07 {

	public static void main(String[] args) {
		// 190514_과제7(java05)
		int a, b, result;
		a = 19;
		b = 5;

		result = a + b;
		System.out.println(a + " + " + b + " = " + result);
		result = a - b;
		System.out.println(a + " - " + b + " = " + result);
		result = a * b;
		System.out.println(a + " * " + b + " = " + result);
		result = a / b;
		System.out.println(a + " / " + b + " = " + result);
		result = a % b;
		System.out.println(a + " % " + b + " = " + result);

	}

}
